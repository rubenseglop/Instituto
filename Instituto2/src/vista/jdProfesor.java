    private void consultaalumno() throws HibernateException {
        Session session = NewHibernateUtil.getSessionFactory().openSession();
        try {
            session.beginTransaction();
            pr = (Profesores) session.get(Profesores.class, usu.getCcodusu());
            id_alumno = new Object[pr.getAsignaturases().size()];
            for (Object asi : pr.getAsignaturases()) {
                as = (Asignaturas) asi;
                id_alumno[id++] = as.getCcodasig();
                jComboBox1.addItem(as.getCnomasig());
            }

        } catch (HibernateException he) {
            he.printStackTrace();
        } finally {
            session.close();
        }
    }

