    private void actualizatabla() {
        if (jComboBox1 != null && jComboBox1.getSelectedItem() != null) {
            DefaultTableModel modelotabla = (DefaultTableModel) jTable1.getModel();
            modelotabla.setNumRows(0);
            Object tabla_campos[] = new Object[5];

            Session session = NewHibernateUtil.getSessionFactory().openSession();

            as = (Asignaturas) session.get(Asignaturas.class, idcombo); // pasarle el codig.

            for (Object exp : as.getExpedienteses()) {
                ex = (Expedientes) exp;
                tabla_campos[0] = ex.getAlumnos().getCcodalu();
                tabla_campos[1] = ex.getAlumnos().getCnomalu();
                tabla_campos[2] = ex.getNota();
                tabla_campos[3] = ex.getAlumnos().getCurso();
                tabla_campos[4] = ex.getId().getEvaluac();
                modelotabla.addRow(tabla_campos);
            }

        }
    }
