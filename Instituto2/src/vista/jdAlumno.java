    private void muestraalumno() throws HibernateException {

        Session session = NewHibernateUtil.getSessionFactory().openSession();
        al = (Alumnos) session.get(Alumnos.class, usu.getCcodusu());
        try {
            session.beginTransaction();
            // Asignaturas y Expedientes de un alumno
            modelotabla = (DefaultTableModel) jTable1.getModel();
            modelotabla.setNumRows(0);
            Object tabla_campos[] = new Object[4];
            for (Object exp : al.getExpedienteses()) {
                ex = (Expedientes) exp;
                tabla_campos[0] = ex.getAsignaturas().getCnomasig();
                tabla_campos[1] = ex.getAsignaturas().getProfesores().getCnomprof();
                tabla_campos[2] = al.getCurso(); 
                tabla_campos[3] = ex.getNota();               
                modelotabla.addRow(tabla_campos);
            }
                 
            session.getTransaction().commit();
        // Cerramos la Session
        } catch (HibernateException he) {
            he.printStackTrace();
        } finally {
            session.close();
        }
    }

