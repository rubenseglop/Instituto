    private void muestraprofesores() throws HibernateException {

        Session session = NewHibernateUtil.getSessionFactory().openSession();

        try {
            Criteria crit = session.createCriteria(Profesores.class);
            List cats = crit.list();
            modelotabla = (DefaultTableModel) jTable1.getModel();
            modelotabla.setNumRows(0);
            Object tabla_campos[] = new Object[3];
            for (Iterator iterator = cats.iterator(); iterator.hasNext();) {
                pr = (Profesores) iterator.next();
                tabla_campos[0] = pr.getCcodprof();
                tabla_campos[1] = pr.getCnomprof();
                tabla_campos[2] = pr.getDepartam();
                modelotabla.addRow(tabla_campos);
            }
        } catch (HibernateException e) {
            e.printStackTrace();
        } finally {
            session.close();
        }
    }

