    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // boton +

        // comprobar que tengo los campos de texto
        if (jtnombre.getText().length() == 0 || jtcurso.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Mensaje dentro de la ventana", "Mensaje en la barra de titulo", JOptionPane.WARNING_MESSAGE);
        } else {
            session = NewHibernateUtil.getSessionFactory().openSession();
            try {
                session.beginTransaction();
                pr = (Profesores) session.get(Profesores.class, modelotabla.getValueAt(jTable1.getSelectedRow(), 0).toString());

                asi = (Asignaturas) session.get(Asignaturas.class, idcombo[jComboBox1.getSelectedIndex()]);
                asi.setProfesores(pr);
                session.update(asi);

                session.getTransaction().commit();
            } catch (HibernateException ex) {
                ex.printStackTrace();
            } finally {
                session.close();
            }
        }
        muestraprofesores();
        muestraasig();

    }//GEN-LAST:event_jButton2ActionPerformed

