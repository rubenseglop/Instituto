    private void muestraasig() {
        Session session = NewHibernateUtil.getSessionFactory().openSession();

        try {
            Criteria crit = session.createCriteria(Asignaturas.class);
            List cats = crit.list();
            jComboBox1.removeAllItems();
            int i = 0;
            idcombo = new String[cats.size()];
            for (Iterator iterator = cats.iterator(); iterator.hasNext();) {
                asi = (Asignaturas) iterator.next();
                jComboBox1.addItem(asi.getCnomasig());
                idcombo[i] = asi.getCcodasig();
                i++;
            }
        } catch (HibernateException e) {
            e.printStackTrace();
        } finally {
            session.close();
        }
    }

    private void muestratablaasig(String codigoasig) {
        if (jTable1.getSelectedRowCount() == 1) {
            Session session = NewHibernateUtil.getSessionFactory().openSession();

            try {
                pr = (Profesores) session.get(Profesores.class, codigoasig);
                modelotabla2 = (DefaultTableModel) jTable2.getModel();
                modelotabla2.setNumRows(0);

                Object tabla_campos[] = new Object[pr.getAsignaturases().size()];
                for (Object exp : pr.getAsignaturases()) {
                    asi = (Asignaturas) exp;
                    tabla_campos[0] = asi.getCnomasig();
                    modelotabla2.addRow(tabla_campos);
                }
            } catch (HibernateException e) {
                e.printStackTrace();
            } finally {
                session.close();
            }
        }
    }
