    private void muestramatriculas() throws HibernateException {

        Session session = NewHibernateUtil.getSessionFactory().openSession();

        try {
            Criteria crit = session.createCriteria(Usuarios.class);
            List cats = crit.list();
            modelotabla = (DefaultTableModel) jTable1.getModel();
            jTable1.setSelectionModel(new ForcedListSelectionModel()); // para seleccionar una unica fila
            modelotabla.setNumRows(0);
            Object tabla_campos[] = new Object[5];
            for (Iterator iterator = cats.iterator(); iterator.hasNext();) {
                us = (Usuarios) iterator.next();
                tabla_campos[0] = us.getCcodusu();
                tabla_campos[1] = us.getCnomusu();
                tabla_campos[2] = us.getPsw();
                String asteriscos = "";
                for (int i = 0; i < us.getPsw().length(); i++) {
                    asteriscos = asteriscos + "*";

                }
                tabla_campos[3] = asteriscos;

                if (us.getTipo().equals("A")) {
                    tabla_campos[4] = "Alumno";
                } else if (us.getTipo().equals("P")) {
                    tabla_campos[4] = "Profesor";
                } else if (us.getTipo().equals("D")) {
                    tabla_campos[4] = "Administrador";
                }
                modelotabla.addRow(tabla_campos);
            }
        } catch (HibernateException e) {
            e.printStackTrace();
        } finally {
            session.close();
        }
    }

