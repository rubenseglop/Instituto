    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        if (jtnombre.getText().length() == 0 || jpass.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Mensaje dentro de la ventana", "Mensaje en la barra de titulo", JOptionPane.WARNING_MESSAGE);
        } else {

            Session session = NewHibernateUtil.getSessionFactory().openSession();
            try {
                session.beginTransaction();

                //sacar ultimo numero codusuario
                Criteria crit = session.createCriteria(Usuarios.class);
                List cats = crit.list();
                int num_mayor = 0;

                for (Iterator iterator = cats.iterator(); iterator.hasNext();) {
                    usu = (Usuarios) iterator.next();
                    if (num_mayor < Integer.parseInt(usu.getCcodusu())) {
                        num_mayor = Integer.parseInt(usu.getCcodusu());
                    }
                }
                num_mayor++;  //este es mi numero mayor

                //dar de alta nuevo alumno 
                SessionFactory sf = session.getSessionFactory();
                session = sf.openSession();

                Transaction tx = session.beginTransaction();

                String tipo = "";
                if (jComboBox1.getSelectedIndex() == 0) {
                    tipo = "A";
                } else if (jComboBox1.getSelectedIndex() == 1) {
                    tipo = "P";
                } else if (jComboBox1.getSelectedIndex() == 2) {
                    tipo = "D";
                }

                Usuarios u1 = new Usuarios(String.valueOf(num_mayor), jtnombre.getText(),
                        tipo, jpass.getText());

                session.save(u1);

                tx.commit();
            } catch (HibernateException ex) {
                // Log the exception here

                System.out.println(ex.getMessage());

            } finally {
                session.close();
            }
            muestramatriculas();
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

