    private void muestraalumnos() throws HibernateException {

        Session session = NewHibernateUtil.getSessionFactory().openSession();

        try {
            Criteria crit = session.createCriteria(Alumnos.class);
            List cats = crit.list();
            modelotabla = (DefaultTableModel) jTable1.getModel();
            modelotabla.setNumRows(0);
            Object tabla_campos[] = new Object[3];
            for (Iterator iterator = cats.iterator(); iterator.hasNext();) {
                Alumnos al = (Alumnos) iterator.next();
                tabla_campos[0] = al.getCcodalu();
                tabla_campos[1] = al.getCnomalu();
                tabla_campos[2] = al.getCurso();
                modelotabla.addRow(tabla_campos);
            }
        } catch (HibernateException e) {
            e.printStackTrace();
        } finally {
            session.close();
        }
    }

    private void muestraexpedientes(String codigoalumno) throws HibernateException {
        if (jTable1.getSelectedRowCount() == 1) {
            Session session = NewHibernateUtil.getSessionFactory().openSession();

            try {
                al = (Alumnos) session.get(Alumnos.class, codigoalumno);
                modelotabla2 = (DefaultTableModel) jTable2.getModel();
                modelotabla2.setNumRows(0);
                jComboBox2.removeAllItems();

                Object tabla_campos[] = new Object[6];
                for (Object exp : al.getExpedienteses()) {
                    ex = (Expedientes) exp;
                    tabla_campos[0] = ex.getAlumnos().getCcodalu();
                    tabla_campos[1] = ex.getAsignaturas().getCcodasig();
                    tabla_campos[2] = ex.getAsignaturas().getCnomasig();
                    jComboBox2.addItem(ex.getAsignaturas().getCnomasig());
                    id_alumno = as.getCcodasig();
                    tabla_campos[3] = ex.getId().getCurso();
                    tabla_campos[4] = ex.getId().getEvaluac();
                    tabla_campos[5] = ex.getNota();
                    modelotabla2.addRow(tabla_campos);
                }
            } catch (HibernateException e) {
                e.printStackTrace();
            } finally {
                session.close();
            }
        }
    }

