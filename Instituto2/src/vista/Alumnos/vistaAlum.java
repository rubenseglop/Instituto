    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // boton +
       if (jCheckBox2.isSelected()== true) {
            // añadir expediente++

            if (esnumero(jtnota.getText()) && jTable1.getSelectedRow() != -1) {
                Session session = NewHibernateUtil.getSessionFactory().openSession();
                try {
                    session.beginTransaction();
                    //id_alumno = jComboBox2.getSelectedIndex();
                    modelotabla = (DefaultTableModel) jTable1.getModel();
//
//                    System.out.println("a " + modelotabla2.getValueAt(jComboBox2.getSelectedIndex(), 0).toString());
//                    System.out.println("b " + modelotabla2.getValueAt(jComboBox2.getSelectedIndex(), 1).toString());
//                    System.out.println("3 " + modelotabla2.getValueAt(jComboBox2.getSelectedIndex(), 3).toString());
//                    System.out.println("4 " + modelotabla2.getValueAt(jComboBox2.getSelectedIndex(), 4).toString());
                    ex = (Expedientes) session.get(Expedientes.class,
                            new ExpedientesId(modelotabla2.getValueAt(jComboBox2.getSelectedIndex(), 0).toString(),
                                    modelotabla2.getValueAt(jComboBox2.getSelectedIndex(), 1).toString(),
                                    modelotabla2.getValueAt(jComboBox2.getSelectedIndex(), 3).toString(),
                                    modelotabla2.getValueAt(jComboBox2.getSelectedIndex(), 4).toString()));
                    //System.out.println("da " + BigDecimal.valueOf(Double.parseDouble(jtnota.getText())));
                    ex.setNota(BigDecimal.valueOf(Double.parseDouble(jtnota.getText())));
                    session.update(ex);
                    session.getTransaction().commit();
                } catch (HibernateException he) {
                    he.printStackTrace();
                } finally {
                    session.close();
                }

            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

